package modelo;

/**
 *
 * @author DEEPIN
 */
public class Avion {

    private String modelo;
    private String nombre;
    private String tipo;

    public Avion() {
    }

    public Avion(String modelo, String nombre, String tipo) {
        this.modelo = modelo;
        this.nombre = nombre;
        this.tipo = tipo;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    @Override
    public String toString() {
        return modelo + " " + nombre + " " + tipo;
    }

}
